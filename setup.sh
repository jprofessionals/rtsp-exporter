#!/usr/bin/env bash

set -euo pipefail

curl -fsSL https://pkgs.tailscale.com/stable/debian/bookworm.noarmor.gpg | sudo tee /usr/share/keyrings/tailscale-archive-keyring.gpg
curl -fsSL https://pkgs.tailscale.com/stable/debian/bookworm.tailscale-keyring.list | sudo tee /etc/apt/sources.list.d/tailscale.list
curl -fsSL https://packages.cloud.google.com/apt/doc/apt-key.gpg | sudo gpg --dearmor -o /usr/share/keyrings/cloud.google.gpg
echo "deb [signed-by=/usr/share/keyrings/cloud.google.gpg] https://packages.cloud.google.com/apt cloud-sdk main" | sudo tee /etc/apt/sources.list.d/google-cloud-sdk.list

sudo apt update
sudo apt install dnsutils ffmpeg vim tailscale google-cloud-cli

sudo tailscale up

sudo ssh-keygen -t ed25519

sudo cp systemd/* /etc/systemd/system/
sudo systemctl daemon-reload
sudo systemctl start pull.timer
sudo systemctl start export.timer
